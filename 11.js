let marks = prompt("Enter your Marks");

if(marks >=90){
    alert("You are in Grade A1")
}
else if(marks >= 80){
    alert ("You are in grade B");
}
else if(marks >= 60){
    alert("You are in grade C");
}
else if(marks >=40){
    alert("You are in grade D")
}

else{
    alert("Better Luck Next Time !");
}